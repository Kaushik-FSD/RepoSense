def clean_llm_response(raw: str) -> str:
    # remove markdown code blocks that LLM sometimes adds
    raw = raw.strip()
    if raw.startswith("```json"):
        raw = raw[7:]
    if raw.startswith("```"):
        raw = raw[3:]
    if raw.endswith("```"):
        raw = raw[:-3]
    return raw.strip()